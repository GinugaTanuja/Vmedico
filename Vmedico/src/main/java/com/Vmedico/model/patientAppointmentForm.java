package com.Vmedico.model;

public class patientAppointmentForm {
	
	private String pusername; 
	private String pemail;
	private String phospitalname;
	private String pdoctorname;
	private String psymptoms;
	private String pslot;
	private String pappointmentdate;
	
	
	//getters and setters
	public String getPusername() {
		return pusername;
	}
	public void setPusername(String pusername) {
		this.pusername = pusername;
	}
	public String getPemail() {
		return pemail;
	}
	public void setPemail(String pemail) {
		this.pemail = pemail;
	}
	public String getPhospitalname() {
		return phospitalname;
	}
	public void setPhospitalname(String phospitalname) {
		this.phospitalname = phospitalname;
	}
	public String getPdoctorname() {
		return pdoctorname;
	}
	public void setPdoctorname(String pdoctorname) {
		this.pdoctorname = pdoctorname;
	}
	public String getPsymptoms() {
		return psymptoms;
	}
	public void setPsymptoms(String psymptoms) {
		this.psymptoms = psymptoms;
	}
	public String getPslot() {
		return pslot;
	}
	public void setPslot(String pslot) {
		this.pslot = pslot;
	}
	public String getPappointmentdate() {
		return pappointmentdate;
	}
	public void setPappointmentdate(String pappointmentdate) {
		this.pappointmentdate = pappointmentdate;
	}
	
	
}
